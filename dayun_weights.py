"""
大运权重表
用于校准每日运势评分，让大运背景体现在分数中
"""

DAYUN_WEIGHTS = {
    "己丑": 10,   # 童年好运，印星生身
    "戊子": 5,    # 印为主，生活拮据但学业运尚可
    "丁亥": -10,  # 七杀+伤官，人生最辛苦的运
    "丙戌": 5,    # 正官+印，有好有坏
    "乙酉": 15,   # 🔥 黄金大运，禄神帮身
    "甲申": 10,   # 也不错，劫财帮身
    "癸未": 5,    # 食神制杀
    "壬午": -5,   # 伤官见官
}


def get_dayun_weight(dayun_name: str) -> int:
    """根据大运名称获取权重值"""
    return DAYUN_WEIGHTS.get(dayun_name, 0)
